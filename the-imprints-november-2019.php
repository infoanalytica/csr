                <h5 class="news-date">What’s the Buzz?</h5>
                <h6 class="news-date" style="color: #007bff;">BAN Plastic? Re-USE Plastic:</h6>      
                <p class="p-size"> India’s leading ecommerce marketplace, Flipkart, has rolled out a new
initiative to minimise the use of plastic and drive its sustainability agenda. As part of this initiative,
Flipkart is conducting a pilot project to collect plastic packaging back from consumers at select hubs
across Mumbai, Bengaluru, Dehradun, Delhi, Kolkata, Pune, and Ahmedabad with an attempt to re-use
the used plastic. The collected material will then be sent to registered vendors to ensure it is disposed
of responsibly and not consigned to landfills. We are proud to be the first-ever e-commerce
marketplace to channelize plastic packaging back into the system through door-to-door collection with
the help of our wish masters," <b style="color: #007bff;">said Rajneesh Kumar, Chief Corporate Affairs Officer, Flipkart Group.</b>"</p>
                
                <p style="color:#A9A9A9; font-size:10px; margin-top:-15px !important;"><i>* Source: The Economic Times</i></p>

                <h6 class="news-date" style="color: #007bff;">First National CSR Awards:</h6>
                <p class="p-size">The President of India, Shri Ram Nath Kovind, presented the National Corporate Social Responsibility
Awards (NCSRA) to companies for their outstanding contribution in area of Corporate Social
Responsibility (CSR) on Tuesday, 29th October 2019 in New Delhi. The National CSR Awards has been
instituted by the Ministry of Corporate Affairs to recognize corporate initiatives in the area of Corporate
Social Responsibility (CSR) to achieve inclusive growth and inclusive and sustainable development.
These awards are the highest recognition in the domain of CSR by the Government of India. Shri Kovind
also suggested companies to contribute more towards social welfare spending for orphans and
disabled people. From now onwards, National CSR awards would be conferred every year on October 2.
List of winners for CSR Awards 2019: <a href="http://bit.ly/2DcDMVp" style="color: #007bff;">http://bit.ly/2DcDMVp</a></p>
 
