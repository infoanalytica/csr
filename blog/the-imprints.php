                <h5 class="news-date">What’s the Buzz?</h5>
                <h6 class="news-date">CSR Spending by Corporates</h6>      
                <p class="p-size">The Parliament has passed amendments to Companies Act. The bill now advocates for stricter laws for corporate social responsibility (CSR) spending by corporates.</p>
                <p class="p-size">The bill includes an amendment that mandates that companies transfer unspent CSR money in a financial year to an escrow account meant for CSR for three years, after which any unused amount must be transferred to a fund specified by the government.</p>
                <p class="p-size">All companies with a net worth of Rs 500 crore or more, turnover of Rs 1,000 crore or more, or net profit of Rs 5 crore or more are required to spend 2% of their average profit of the previous three years on CSR activities every year.</p>
                <p class="p-size">Finance Minister, Nirmala Sitharaman said, <i>“the government is also strengthening enforcement provisions.”</i></p> 
                <p style="color:#A9A9A9; font-size:10px; margin-top:-15px !important;"><i>* Source: The Economic Times</i></p>

