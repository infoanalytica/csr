Sunil Parekh, and Chairperson FICCI FLO, Babita Jain. NGOs and Social Enterprises like Self Employed Women’s
Association (SEWA), Blind People Association, FICCI FLO, Saath, Apang Manav Mandal, Manav Sadhna, and 25 more
enlightened the event with their participation at the flea. The event witnessed more than 1000+ footfalls.</p>


