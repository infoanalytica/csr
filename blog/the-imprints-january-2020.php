                <h5 class="news-date">Editor’s Note</h5>
                <p class="p-size"> Imagine that when you buy, the child who has no parents smiles? When you ask about the embroidery, the shy
housewife glows! Or when you spend a lot, still come back home happy and fulfilled!!
</p>
                <p class="p-size">Welcome to the special edition of our CSR newsletter series, "Amdavad Social – the first social flea of the city" and
an attempt to celebrate Opportunity, Empowerment, and Togetherness within the social sector of the city.
</p>
                <p class="p-size">Amdavad, a smart and swaacch city of social goodness with NGOs, Trusts, Corporate Initiatives, SHG’s, and
Government Projects, working for the social betterment. However, a lot of them are working in silos. Amdavad
Social brings these Social Enterprises to a common platform where the community can share their synergies,
ideas, and learn for a better and more significant impact. Also, the citizens get to interact with them and get an
opportunity to know about social betterment, understanding how they can be a part of this too.</p>
 <p class="p-size">
Let’s have the entire social sector under one roof and see the elements of Amdavad Social!</p>
                <p class="p-size"><strong>- Pranav Gupta, Project Manager - CSR</strong></p>

          

