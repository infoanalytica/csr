                <h6 class="news-date">Food Kit Distribution:</h6>
                <p class="p-size" style="margin-top: 0px; margin-right: 0px;">It is only now that we are aware of the hardships that a migrant worker, a slum dweller, a safai
karamchari, security guards or your housemaid faces. The struggle for their wages, food, health has
now started affecting us and the society around. iA Foundation thought of distributing Ration Kits to
the underprivileged community deprived of food. These food kit contains our daily essentials like
(Rice, Atta, Sugar, Tea, Oil, and other commodities). One food kit serves a family of four for at-least
ten days.<br>
<b>If you wish to contribute to our cause at: https://bit.ly/3eplKko</b></p>

