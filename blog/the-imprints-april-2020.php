                <h5 class="news-date">Founder’s Message</h5>    
                <p class="p-size">"Corona." The word after every few ones these days. And it's been some weeks now. While we are locked
down and have WFH or enough ration or Netflix shows buffered. We didn't stop thinking about the maids,
laundry guy, hawkers, daily wagers, laborers, migrants, or older people. A small step towards this led to a
marathon of activities, and we feel relatively content. The best part has been helping and support coming
from various individuals, organizations, Corporates, AMC, and the Police team. We have been able to provide
meals, ration kit, and tea service to about 1,00,000 people and counting in the city. Have raised funds to get
this going, built a team of volunteers, tracking it geographically, sharing stories, and will continue this till we
can. A shout out to The Ahmedabad Project, Foodaholics in Ahmedabad, Sante Spa, VSSM, Hi-tech
outsourcing, RHA, Radio 104 and more for trusting and collaborating with us. And most of all, the iAeans for
your constant backing.</p>


