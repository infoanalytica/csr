                <h6 class="news-date" align="right">Ahmedabad Runs on Chai:</h6>
                <p class="p-size" style="margin-top: 0px; margin-left: 0px;" align="right">As the city wears a deserted look, it is hard to even find the chai walas, the suppliers of fuel to that
makes Ahmedabad run. They say 70% of the human body is water, we think in case of the average
Gujarati, it is 50% chai. This is our small homage in collaboration with Te.Cha to those still out there
working for our city - the traffic cops, the street vendors selling vegetables, the safai karamcharis and
yes, the homeless and daily wagers wherever we find them. </p>

