                <h6 class="news-date">Arree, Aarey! (The choice between Development/Environment):</h6>      
                <p class="p-size">The death toll was surrounded for 2,815 and still counting
trees to make way for Metro car shed in Mumbai's Aarey Colony. While green activists have called the Mumbai metro's
move to cut trees, the government justified it by saying that the development is meant for a greater good. Shaina NC, a
leader of the ruling party (BJP), said that "We are taking only two percent of Aarey land and planting over 20,000 trees
across Maharashtra in its lieu,". She also claimed that the project will be sustainable as the CM's office will directly
monitor the saplings and that the government was sure that they would have a 35 percent survival rate. Meanwhile,
prohibitory orders under Section 144 of CrPC imposed in the area near the metro-rail project site in Aarey forest. The
environment conservationists were left chanting the slogan of "Choose Aarey, not concrete Grey!"</p>
                
