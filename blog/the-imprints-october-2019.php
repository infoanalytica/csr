                <h6 class="news-date">Project Good Governance – Nov 10, 2019</h6>
                <img src="../img/nc_oct_pannah.png" alt="alt text" align="left" hspace="25" vspace="25">
                <p class="p-size">Can you sustain the system without an Aadhar Card, Pan Card, bank account, Ration Card,
Maa Card, and other government subsidies? These people have since the past 50 years!
infoAnalytica Foundation with Pannah (a Public Charitable Trust who provides a one-step
centre with an aim to provide access to the basic services by the government is setting up a
help centre at Ramdevnagar slum areas for the people who are deprived of all government
schemes and services which they deserve. With sensitizing the people from slums to
providing them with a direction on how to avail and get benefitted from the government
schemes, iA foundation and Pannah will work extensively in Ramdevnagar Slums for the
next three months.</p>
                
