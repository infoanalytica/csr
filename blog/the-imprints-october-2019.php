                <h5 class="news-date">Rewind, Relive!</h5>
                 
                <h6 class="news-date">Eco dun at DAIICT – September 29, 2019:</h6>
                <img src="../img/nl_oct_eco_run.png" width="300" alt="alt text" align="right" hspace="25" vspace="25">
                <p class="p-size">DA-IICT Gandhinagar organized a 7 KM long run, supported by iA Foundation. The inspiration for the
run was to provide the youth with an opportunity to voice their opinions on issues that are close to
the society while keeping their health at the center. With the theme of #HumFitTohIndiaFit, the event
was inaugurated by the Shri D.D. Kapadiya Sir - Secretary of Sports Authority of Gujarat (SAG) and
Mayor of Gandhinagar. More than 700 people across the city participated in the event, and we hope
that all the participants leave the race today with an increased appreciation for our environment and
will continue to push the boundaries to adopt a healthy and sustainable lifestyle in their daily lives.
Only then can we be future-ready together."</p>
               

                <h6 class="news-date">"The idea of wilderness needs no defence, it only needs defenders."― Edward Abbey:</h6>
                <img src="../img/nl_oct_escaping_the_wilderness.png" width="300" alt="alt text" align="left" hspace="25" vspace="25">
                <p class="p-size">DivingDeep and Decathlon Ahmedabad as a part of "World CleanUp Day 2019", organized
clean-up hikes at Thangadh, Jessore Sanctuary, and Amirgadh. The purpose was to clean
up the identified litter-spots and collect the trash while hiking. Participants also performed
hiking and scrambling during specific sessions to strengthen their connections with
outdoors and wild grounds. As a team, they collected approximately 550 to 570 kgs of
separate solid waste, especially plastic, metal, and glass from these outdoors. More than
ten iA employees were part of this movement to raise local awareness and implement
lasting changes to end the waste epidemic once and for all at these specific locations.</p>

