                <h5 class="news-date">ABOUT THE AHMEDABAD PROJECT :</h5>
The Ahmedabad Project aims to create equitable
and empathetic cities across India. As India
urbanizes rapidly, cities are expanding but
without considering the massive influx of
migrants from villages who come to the city
for earning a livelihood. Cities are also facing
challenges of traffic, garbage disposal, water
shortages, and disappearance of the city's green
cover. The Ahmedabad Project works through
policy engagement with citizens, workshops on
governance, policy cafes and fellowships on civic
issues. We think our cities will be more livable
when enough citizens understand who their ward
councillors are, what their corporation does,
and how to evaluate and make better policies.</p>


