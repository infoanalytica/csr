                <h6 class="news-date">From CSR Industry:</h6>
                <p class="p-size">Allaying industry concerns, Finance Minister Nirmala Sitharaman said violations of CSR norms under the Companies Act
2013 will be treated only as a civil liability and not as a criminal offence. The committee led by Injeti Srinivas, Secretary –
MCA recommends corporates to focus at adopting sustainable development goals which would additionally include
sports promotion, senior citizens' welfare, welfare of differently-abled persons, disaster management and heritage
protection.</p>
 
