                <h5 class="news-date">What’s the Buzz?</h5>
                <h6 class="news-date">Water, a scarcity?!</h6>      
                <p class="p-size">The water level in the Sardar Sarovar Dam at Kevadiya in Narmada
district reached its highest capacity at 138.68 metres. The dam aims to provide drinking
water to 131 urban centres and 9,633 villages (53 per cent of total 18,144 villages in
Gujarat) and irrigation facilities for 18.54 hectares of land, covering 3,112 villages in 15
districts. The dam is currently holding 3.16 lakh mcft (million cubic feet) of water, which is
94.612% of its actual storage capacity, said Gujarat Government. PM Modi inaugurated
the dam on September 17, 2017.”</p>
                
