                <h5 class="news-date" style="color: #007bff;">ABOUT INFOANALYTICA FOUNDATION</h5>    
                <p class="p-size">At InfoAnalytica, we understand that we are an integral
part of a community beyond our business. Through
InfoAnalytica Foundation, our efforts are focused on
building sustainable, happy communities. We touch
thousands of lives yearly through our interventions in quality
education, preventive healthcare, heritage conservation,
holistic development and, empowering women. We
collaborate with academic institutions, development, and
non-profit organizations to attain shared goals.</p>
            <br>
             <h5 class="news-date" style="color: #007bff;">ABOUT THE AHMEDABAD PROJECT</h5>    
                <p class="p-size">The Ahmedabad Project aims to create equitable and
empathetic cities across India. As India urbanizes rapidly,
cities are expanding but without considering the massive
influx of migrants from villages who come to the city for
earning a livelihood. Cities are also facing challenges of
traffic, garbage disposal, water shortages, and
disappearance of the city's green cover. The Ahmedabad
Project works through policy engagement with citizens,
workshops on governance, policy cafes and fellowships on
civic issues. We think our cities will be more livable when
enough citizens understand who their ward councilors are,
what their corporation does, and how to evaluate and make
better policies.</p>
            <br>
             <h5 class="news-date" style="font-size: 50px; background-color: #8c0201; color: #ffffff;">OUR RESPONSE TO COVID 19</h5>

                <p class="p-size" style="margin-top: 0px; margin-right: 0px;">When COVID - 19 lockdown was announced, we were in the middle of launching a few
initiatives on working with the informal sector. These centered around finding ways to create
social safety nets like pensions, insurance, and maternity leaves for those working as
housemaids, construction workers, cooks, nannies, drivers, security guards, and others.
However, we realized that the immediate need during lockdown was of food and rations.</p>
