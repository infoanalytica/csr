                <h5 class="news-date" style="font-size: 25px; background-color: #007bff; color: #ffffff; margin-right: 369px; margin-top: 242px;">THE CREATIVE FELLOWSHIP</h5>
                <p class="p-size" style="margin-top: 0px; margin-right: 0px;">The Creative Fellowship was an effort to bring the
artistic community together to sensitize citizens
towards the plight of the poor in India. A team of
30 designers from across the country (From NID,
CEPT, NIFT, SRISTI, Anant Fellowship, and so on)
got together to create content related to dignity in
the informal sector. Posters, videos, graffiti, and
social media content, were designed on
construction workers, maids, safai workers, guards,
and their daily struggles. For example, a poster
showing how we enjoy at least 150 days of holidays
yearly (104 days of weekends off + medical +
casual + festival leaves) and domestic maids get
none and so on.
</p>   
 <h5 class="news-date" style="font-size: 25px; background-color: #007bff; color: #ffffff; margin-right: 30px;
    margin-top: 0px;">DIGITAL DOST (COLLATING
AND DIGITIZING DATABASE
OF MIGRANTS)</h5>
                <p class="p-size" style="margin-top: 0px; margin-right: 0px;">Digital Dost aims to document unorganized sector
migrants specifically in urban areas working across
manufacturing, construction work, rag-picking, maids,
drivers, guards, and other informal professions. The
data with the state isn’t actionable, or the data is
scattered among different non-profits with no
coordination or there isn’t enough data. Digital Dost is
an actionable, dynamic database that will help us
transfer funds, run upskilling initiatives and get
insurance and pension products to migrant workers.
</p>   
